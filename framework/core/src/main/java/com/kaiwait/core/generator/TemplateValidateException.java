package com.kaiwait.core.generator;

public class TemplateValidateException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8981838168794141548L;

	public TemplateValidateException() {
		super();
	}

	public TemplateValidateException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TemplateValidateException(String message, Throwable cause) {
		super(message, cause);
	}

	public TemplateValidateException(String message) {
		super(message);
	}

	public TemplateValidateException(Throwable cause) {
		super(cause);
	}

}
